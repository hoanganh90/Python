# Everything is an object in Python
# In Python, everything is an object, including functions, classes, and even modules.
# This means that you can pass functions as arguments, return them from other functions, and assign them to variables.
print(type(1)) # Output: <class 'int'>
print(type('Hie'.count())) # Output: <class 'builtin_function_or_method'> can be accessed by dot operator.